#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	char	*ptr;

	ptr = NULL;
	while (*s != '\0')
	{
		if (*s == c)
			ptr = (char *)s;
		s++;
	}
	if (ptr != NULL)
		return (ptr);
	else if (c == '\0')
		return (s);
	else
		return (NULL);
}
